	ld de, 0
	ld hl, input
	
_check_character:
	ld a, (hl)
	cp 0
	jp z, the_end

	inc hl
	cp 40 ; Left bracket
	jp nz, _go_down

_go_up:
	inc de
	jp _check_character

_go_down: 
	dec de
	jp _check_character

the_end:
	ld a, d
	call PrintNum8
	ld a, e
	call PrintNum8
	call NewLine	

	ret

PrintNum8:
	ld b, 8
_loop
	push af
		and %10000000
		jr nz, _print1
		ld a, &30
		call &BB5A
		jr _done
_print1:
		ld a, &31
		call &BB5A
_done
		ld a, b
		dec a
		jr z, _exit
		ld b, a	
	pop af
	rla
	jr _loop
_exit:
	pop af
	ret

NewLine:
	ld a, 13
	call &BB5A
	ld a, 10
	call &BB5A
	ret

